// variables and datatypes

public class variableanddatatypes {
    public static void main(String[] args) {
        int money = 2000;
        String name = "Abhi";
        float f = 123.45f;// f or F

        System.out.println(money);
        money = 5000;
        System.out.println(money);

        System.out.println(f);
        System.out.println(name);

    }
}
